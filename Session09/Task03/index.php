  </header>

  <div class="card notice">
    <p>Feladat: jegyzetek mentése adatbázisba, listázás, törlés (mind prepared statement-tel).</p>
    <p class="small">Szerkesztés külön feladatként jön a következő feladatban / következő héten, itt C/R/D a fókusz.</p>
  </div>

  <?php if ($error): ?><div class="card err"><strong>Hiba:</strong> <?php echo h($error); ?></div><?php endif; ?>
  <?php if ($flash): ?><div class="card ok"><strong><?php echo h($flash); ?></strong></div><?php endif; ?>

  <div class="card">
    <h2>Új jegyzet</h2>
    <form method="post" novalidate>
      <input type="hidden" name="action" value="create">
      <label for="title">Cím</label>
      <input id="title" name="title" type="text" value="<?php echo h($title); ?>">
      <?php if (isset($errors['title'])): ?><div class="field-error"><?php echo h($errors['title']); ?></div><?php endif; ?>
      <label for="body">Tartalom</label>
      <textarea id="body" name="body" rows="5"><?php echo h($body); ?></textarea>
      <?php if (isset($errors['body'])): ?><div class="field-error"><?php echo h($errors['body']); ?></div><?php endif; ?>
      <div style="margin-top:12px"><button type="submit">Mentés</button></div>
    </form>
  </div>

  <div class="card">
    <h2>Jegyzetek (<?php echo (int)count($rows); ?>)</h2>
    <?php if (!$rows): ?><p class="small">Nincs jegyzet.</p>
    <?php else: ?>
      <table>
        <thead>
          <tr>
            <th>Idő</th>
            <th>Cím</th>
            <th>Tartalom</th>
            <th>Művelet</th>
          </tr>
        </thead>
        <tbody>
          <?php foreach ($rows as $r): ?>
            <tr>
              <td class="small"><?php echo h((string)$r['created_at']); ?></td>
              <td><?php echo h((string)$r['title']); ?></td>
              <td><?php echo h((string)$r['body']); ?></td>
              <td>
                <form method="post" style="margin:0" onsubmit="return confirm('Törli a jegyzetet?')">
                  <input type="hidden" name="action" value="delete">
                  <input type="hidden" name="id" value="<?php echo (int)$r['id']; ?>">
                  <button type="submit">Törlés</button>
                </form>
              </td>
            </tr>
          <?php endforeach; ?>
        </tbody>
      </table>
    <?php endif; ?>
  </div>

  <footer class="small">Generated by PHP</footer>
</body>

