<?php

declare(strict_types=1);

// htmlspecialchars(): HTML-escape (XSS megelőzés)
function h(string $v): string
{
  return htmlspecialchars($v, ENT_QUOTES | ENT_SUBSTITUTE, 'UTF-8');
}
?>
<?php

declare(strict_types=1);

require_once __DIR__ . '/db.php';

$title = (string)($_POST['title'] ?? '');
$body  = (string)($_POST['body'] ?? '');
$errors = [];
$flash = null;
$error = null;
$rows = [];

try {
  $mysqli = db();

  // DELETE (POST)
  if ($_SERVER['REQUEST_METHOD'] === 'POST' && ($_POST['action'] ?? '') === 'delete') {
    $id = (int)($_POST['id'] ?? 0);
    $stmt = $mysqli->prepare('DELETE FROM notes WHERE id = ?');
    $stmt->bind_param('i', $id);
    $stmt->execute();
    $stmt->close();
    header('Location: ' . $_SERVER['PHP_SELF']);
    exit;
  }

  // CREATE (POST)
  if ($_SERVER['REQUEST_METHOD'] === 'POST' && ($_POST['action'] ?? '') === 'create') {
    $t = trim($title);
    $b = trim($body);

    if ($t === '') $errors['title'] = 'A cím kötelező.';
    if ($b === '') $errors['body'] = 'A tartalom kötelező.';
    if (mb_strlen($t, 'UTF-8') > 120) $errors['title'] = 'A cím max 120 karakter.';

    if (!$errors) {
      $stmt = $mysqli->prepare('INSERT INTO notes (title, body) VALUES (?, ?)');
      $stmt->bind_param('ss', $t, $b);
      $stmt->execute();
      $stmt->close();
      header('Location: ' . $_SERVER['PHP_SELF'] . '?ok=1'); // PRG
      exit;
    }
  }

  $flash = isset($_GET['ok']) ? 'Jegyzet elmentve.' : null;

  $res = $mysqli->query('SELECT id, title, body, created_at FROM notes ORDER BY created_at DESC');
  $rows = $res->fetch_all(MYSQLI_ASSOC);

  $mysqli->close();
} catch (Throwable $e) {
  $error = $e->getMessage();
}
?>
