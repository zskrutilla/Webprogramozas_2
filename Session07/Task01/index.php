  <div class="card">
    <form method="post" novalidate class="row" style="align-items:end">
      <div>
        <label for="mode">Átváltás</label>
        <select id="mode" name="mode">
          <option value="c2f" <?php echo $mode === 'c2f' ? 'selected' : ''; ?>>Celsius → Fahrenheit</option>
          <option value="f2c" <?php echo $mode === 'f2c' ? 'selected' : ''; ?>>Fahrenheit → Celsius</option>
        </select>
        <?php if (isset($errors['mode'])): ?><div class="field-error"><?php echo h($errors['mode']); ?></div><?php endif; ?>
      </div>
      <div>
        <label for="value">Érték</label>
        <input id="value" name="value" type="text" value="<?php echo h($valueRaw); ?>">
        <?php if (isset($errors['value'])): ?><div class="field-error"><?php echo h($errors['value']); ?></div><?php endif; ?>
      </div>
      <div><button type="submit">Számítás</button></div>
    </form>
  </div>

  <?php if ($result): ?>
    <div class="card ok">
      <h2>Eredmény</h2>
      <table>
        <tr>
          <th>Celsius</th>
          <td><?php echo h(number_format((float)$result['c'], 2, ',', ' ')); ?> °C</td>
        </tr>
        <tr>
          <th>Fahrenheit</th>
          <td><?php echo h(number_format((float)$result['f'], 2, ',', ' ')); ?> °F</td>
