<?php

declare(strict_types=1);

// htmlspecialchars(): HTML-escape (XSS megelőzés)
function h(string $v): string
{
  return htmlspecialchars($v, ENT_QUOTES | ENT_SUBSTITUTE, 'UTF-8');
}
?>
<?php

declare(strict_types=1);

require_once __DIR__ . '/src/Utils/Autoloader.php';

use App\Utils\Autoloader;
use App\Domain\Temperature;

Autoloader::register('App', __DIR__ . '/src');

$mode = (string)($_POST['mode'] ?? 'c2f');
$valueRaw = (string)($_POST['value'] ?? '');
$errors = [];
$result = null;

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
  $v = filter_var($valueRaw, FILTER_VALIDATE_FLOAT);
  if ($valueRaw === '' || $v === false) $errors['value'] = 'Kérem, adjon meg érvényes számot.';
  if ($mode !== 'c2f' && $mode !== 'f2c') $errors['mode'] = 'Ismeretlen mód.';

  if (!$errors) {
    $temp = ($mode === 'c2f') ? Temperature::fromCelsius((float)$v) : Temperature::fromFahrenheit((float)$v);
    $result = ['c' => $temp->celsius(), 'f' => $temp->fahrenheit()];
  }
}
?>
