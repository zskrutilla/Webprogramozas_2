      <?php for ($p = 1; $p <= (int)$data['pages']; $p++): ?>
        <?php if ($p === $query->page): ?>
          <strong><?php echo $p; ?></strong>
        <?php else: ?>
          <a href="<?php echo h(pageLink($p, $query->q)); ?>"><?php echo $p; ?></a>
        <?php endif; ?>
        <?php if ($p < (int)$data['pages']) echo ' · '; ?>
      <?php endfor; ?>
    </div>
