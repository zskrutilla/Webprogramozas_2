<?php

declare(strict_types=1);

require_once __DIR__ . '/helpers.php';
require_once __DIR__ . '/db.php';
require_once __DIR__ . '/src/Utils/Autoloader.php';

use App\Utils\Autoloader;
use App\Domain\ProductQuery;
use App\Repo\ProductSearchRepository;
use App\Service\ProductSearchService;

Autoloader::register('App', __DIR__ . '/src');

$q = trim((string)($_GET['q'] ?? ''));
$page = (int)(filter_var((string)($_GET['page'] ?? '1'), FILTER_VALIDATE_INT) ?: 1);
$perPage = 5;

$error = null;

try {
  $db = Db::conn();
  $service = new ProductSearchService(new ProductSearchRepository($db));

  $query = new ProductQuery($q, $page, $perPage);
  $data = $service->search($query);
} catch (Throwable $e) {
  $error = $e->getMessage();
  $data = ['total' => 0, 'pages' => 1, 'rows' => []];
  $query = new ProductQuery($q, $page, $perPage);
}

function pageLink(int $p, string $q): string
{
  return '?q=' . urlencode($q) . '&page=' . $p;
}
